    protected void datatable(){
        Object[] Baris ={"Id Karyawan","Nama","Id Absen","Jabatan","Tunjangan","Lembur","Bulan","Gaji Kotor","Gaji Bersih"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel.setModel(tabmode);
        String sql = "select * from gaji";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("id_karyawan");
                String b = hasil.getString("nama");
                String c = hasil.getString("absen");
                String d = hasil.getString("jabatan");
                String e = hasil.getString("tunjangan");
                String f = hasil.getString("lembur");
                String g = hasil.getString("gaji_kotor");
                String j = hasil.getString("bulan");
                String k = hasil.getString("gaji_bersih");
                
                String[] data={a,b,c,d,e,f,g,j,k};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }
    
